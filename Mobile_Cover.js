function create_model_list(data) {
  document.querySelector("#model").innerHTML = "";

  data.forEach((element) => {
    element.list.forEach((element) => {
      let option = document.createElement("option");

      option.innerText = element;

      document.querySelector("#model").append(option);
    });
  });
}

function get_brand(event) {
  let value = event.target.value;

  let maodel_data = mobile_array.filter((element) => {
    return element.category == value;
  });
  create_model_list(maodel_data);
}

