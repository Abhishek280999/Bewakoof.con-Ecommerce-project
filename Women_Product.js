let women_filter_data;

function category_option() {
  var select_option = document.getElementById("category").value;

  women_filter_data = mens_category.filter(function (element) {
    return element.category == select_option;
  });
  product_page(women_filter_data);

  let women_data = localStorage.setItem(
    "women_data",
    JSON.stringify(women_filter_data)
  );
}

function sortList(arr, order) {
  if (order == "Low") {
    return arr.sort((a, b) => a.price - b.price);
  } else if (order == "High") {
    return arr.sort((a, b) => b.price - a.price);
  }
}

function sort_option() {
  var select_option = document.getElementById("sort_div").value;
  let tshirt = women_filter_data[0];
  let final_data = sortList(tshirt.list, select_option);
  let arr = [];
  let obj = {
    list: final_data,
  };
  arr.push(obj);
  product_page(arr);
}
